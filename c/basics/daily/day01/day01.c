// Day 01 - Hello World
// Learn: Basic C program structure, printf(), compilation

#include <stdio.h>

int main(void) {
    // Print to standard output
    printf("Hello, World!\n");
    printf("Welcome to C programming!\n");

    // The \n is a newline character
    // The return 0 indicates successful program execution
    return 0;
}

/*
 * To compile and run:
 *   gcc -Wall -Wextra -g day01.c -o day01
 *   ./day01
 *
 * Expected output:
 *   Hello, World!
 *   Welcome to C programming!
 */
